print('\033[4;33m===== EXERCÍCIO 004 =====\033[m')
valor1 = input('Digite algo ao lado: ')
print(f'O tipo primitivo do valor acima é: \033[1;35m{type(valor1)}\033[m')
print(f'Só tem espaços? \033[1;35m{valor1.isspace()}\033[m')
print(f'É um número? \033[1;35m{valor1.isnumeric()}\033[m')
print(f'É alfabético? \033[1;35m{valor1.isalpha()}\033[m')
print(f'É alfanumérico? \033[1;35m{valor1.isalnum()}\033[m')
print(f'Está em maiúsculo? \033[1;35m{valor1.isupper()}\033[m')
print(f'Está em minúsculo? \033[1;35m{valor1.islower()}\033[m')
print(f'Está capitalizada? \033[1;35m{valor1.istitle()}\033[m')
print(f'É decimal? \033[1;35m{valor1.isdecimal()}\033[m')